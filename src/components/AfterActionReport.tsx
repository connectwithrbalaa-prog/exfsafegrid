function formatDuration(start: Date, end: Date | null): string {
  if (!end) return "Ongoing";
  const ms = end.getTime() - start.getTime();
  const h = Math.floor(ms / 3600000);
  const m = Math.round((ms % 3600000) / 60000);
  return `${h}h ${m}m`;
}

export default function AfterActionReport() {
  const [selectedEventId, setSelectedEventId] = useState<string>(MOCK_EVENTS[0].eventId);
  const [generating, setGenerating] = useState(false);
  const [sections, setSections] = useState<ReportSection[]>([]);
  const [generatedAt, setGeneratedAt] = useState<Date | null>(null);

  const selectedEvent = MOCK_EVENTS.find((e) => e.eventId === selectedEventId)!;

  const generateReport = useCallback(async () => {
    setGenerating(true);
    setSections([]);

    const e = selectedEvent;
    const duration = formatDuration(e.startTime, e.endTime);

    // In production: call Supabase edge function which invokes Claude API with full event data
    // Here we use a structured local generation to demonstrate the output format
    await new Promise((r) => setTimeout(r, 1500)); // simulate API call

    const newSections: ReportSection[] = [
      {
        title: "Executive Summary",
        expanded: true,
        content: `On ${e.startTime.toLocaleDateString()}, ${e.region} experienced ${e.eventName} requiring Public Safety Power Shutoff (PSPS) activation. The event affected ${e.affectedCustomers.toLocaleString()} customers across the service territory, with ${e.medicalBaseline} medical baseline customers receiving priority outreach.

The utility activated PSPS Protocol ${e.pspsPhase.split("—")[0].trim()} at ${e.startTime.toLocaleTimeString()} and restored service within ${duration}. All ${e.medicalBaseline} medical baseline customers were contacted within 2 hours of PSPS notification, exceeding the CPUC-mandated 4-hour requirement.

Total ${e.alertsSent.toLocaleString()} notifications were dispatched via SMS and automated voice. ${e.helpReplies} customers requested live agent assistance, all of which were resolved within 30 minutes.`,
      },
      {
        title: "Event Timeline",
        expanded: false,
        content: `T+0:00 — ${e.startTime.toLocaleString()} · PSPS watchlist generated by AI planning agent. ${e.affectedCustomers} customers identified.
T+0:30 — Pre-event SMS notifications dispatched to all affected customers.
T+1:00 — Medical baseline outreach calls initiated. ${e.medicalBaseline} customers contacted.
T+2:00 — ${e.pspsPhase}. De-energization sequence executed across ${Math.ceil(e.affectedCustomers / 50)} circuits.
T+4:00 — ${e.crewDeployments} field crew deployed for patrol and condition assessment.
T+8:00 — ${e.hazardReports} hazard reports submitted via Field Crew App.
T+${duration} — Restoration complete. All customers restored. Post-event notifications sent.`,
      },
      {
        title: "Customer Impact & Outreach",
        expanded: false,
        content: `Total Affected: ${e.affectedCustomers.toLocaleString()} customers
Medical Baseline: ${e.medicalBaseline} (priority outreach — 100% contacted within 2h)
Notifications Sent: ${e.alertsSent.toLocaleString()} (${(e.alertsSent / e.affectedCustomers).toFixed(1)}x per customer)
Help Replies Received: ${e.helpReplies} (${((e.helpReplies / e.affectedCustomers) * 100).toFixed(1)}% escalation rate)
Average Response Time to Help Requests: 18 minutes

Financial Assistance:
- REACH applications processed: ${Math.floor(e.affectedCustomers * 0.04)}
- Emergency credits issued: ${Math.floor(e.affectedCustomers * 0.08)}
- Backup power equipment loans from CRC: ${Math.floor(e.medicalBaseline * 0.6)}`,
      },
      {
        title: "Field Crew & Operations",
        expanded: false,
        content: `Crew Deployments: ${e.crewDeployments} (${Math.ceil(e.crewDeployments / 6)} crews of ~6)
Patrol Circuits Covered: ${Math.ceil(e.crewDeployments * 1.4)}
Hazard Reports Filed: ${e.hazardReports}
  - Vegetation contact: ${Math.floor(e.hazardReports * 0.4)}
  - Equipment anomalies: ${Math.floor(e.hazardReports * 0.3)}
  - Access road issues: ${Math.floor(e.hazardReports * 0.2)}
  - Other: ${Math.floor(e.hazardReports * 0.1)}

All hazard reports geo-tagged via Field Crew PWA and uploaded to Supabase Storage.
Post-patrol inspection photos archived: ${e.hazardReports * 3} images.`,
      },
      {
        title: "Compliance & Regulatory Notes",
        expanded: false,
        content: `CPUC General Order 95 Compliance:
☑ Medical baseline notification within 4-hour window — COMPLIANT (actual: ~2h)
☑ Customer notification 24h prior to PSPS — COMPLIANT
☑ CRC open and staffed during de-energization — COMPLIANT
☑ Hazard report documentation complete — COMPLIANT

NERC CIP-014 Physical Security:
☑ All transmission substations patrolled — COMPLIANT
☑ Physical security incidents: 0

Regulatory Submission Checklist:
□ File PSPS Event Report with CPUC within 10 days
□ Submit Customer Notification Log (Appendix A)
□ Provide Outage Duration report (Rule 20A compliance)
□ Complete Annual PSPS De-brief form

Event ID: ${e.eventId} | Generated: ${new Date().toLocaleString()}`,
      },
    ];

    setSections(newSections);
    setGeneratedAt(new Date());
    setGenerating(false);
    toast.success("Report generated successfully");
  }, [selectedEvent]);

  const exportPDF = () => {
    window.print();
    toast.success("PDF export triggered — use browser print dialog");
  };

  const toggleSection = (idx: number) => {
    setSections((prev) =>
      prev.map((s, i) => (i === idx ? { ...s, expanded: !s.expanded } : s))
    );
  };

  return (
    <div className="space-y-5">
      {/* Event selector */}
      <div className="flex items-center gap-4 flex-wrap">
        <div className="flex-1 min-w-[260px]">
          <label className="text-[10px] uppercase tracking-wider text-white/40 mb-1.5 block">Select Event</label>
          <select
            value={selectedEventId}
            onChange={(e) => { setSelectedEventId(e.target.value); setSections([]); }}
            className="w-full px-3 py-2 rounded-lg bg-white/5 border border-white/10 text-sm text-white focus:outline-none focus:ring-1 focus:ring-blue-500/50"
          >
            {MOCK_EVENTS.map((ev) => (
              <option key={ev.eventId} value={ev.eventId}>{ev.eventId} — {ev.eventName}</option>
            ))}
          </select>
        </div>
        <button
          onClick={generateReport}
          disabled={generating}
          className="flex items-center gap-2 px-4 py-2 rounded-lg bg-blue-600/20 border border-blue-500/30 text-blue-300 text-sm font-medium hover:bg-blue-600/30 disabled:opacity-40 transition-colors"
        >
          {generating ? <RefreshCw className="w-4 h-4 animate-spin" /> : <FileText className="w-4 h-4" />}
          {generating ? "Generating…" : "Generate Report"}
        </button>
        {sections.length > 0 && (
          <button
            onClick={exportPDF}
            className="flex items-center gap-2 px-4 py-2 rounded-lg bg-emerald-600/20 border border-emerald-500/30 text-emerald-300 text-sm font-medium hover:bg-emerald-600/30 transition-colors"
          >
            <Download className="w-4 h-4" />
            Export PDF
          </button>
        )}
      </div>

      {/* Event metrics */}
      <div className="rounded-xl border border-white/[0.08] bg-white/[0.03] p-4">
        <div className="flex items-center gap-2 mb-4">
          <AlertTriangle className="w-4 h-4 text-orange-400" />
          <h3 className="text-sm font-semibold">{selectedEvent.eventName}</h3>
          <span className="text-[10px] text-white/30 font-mono">{selectedEvent.eventId}</span>
        </div>
        <div className="grid grid-cols-2 md:grid-cols-4 gap-4">
          {[
            { icon: <Users className="w-4 h-4 text-blue-400" />, label: "Affected", val: selectedEvent.affectedCustomers.toLocaleString() },
            { icon: <Clock className="w-4 h-4 text-amber-400" />, label: "Duration", val: formatDuration(selectedEvent.startTime, selectedEvent.endTime) },
            { icon: <Zap className="w-4 h-4 text-yellow-400" />, label: "Alerts Sent", val: selectedEvent.alertsSent.toLocaleString() },
            { icon: <CheckCircle2 className="w-4 h-4 text-emerald-400" />, label: "Crew Deployed", val: selectedEvent.crewDeployments },
          ].map((m) => (
            <div key={m.label} className="flex items-center gap-2">
              {m.icon}
              <div>
                <p className="text-[10px] text-white/30">{m.label}</p>
                <p className="text-base font-bold text-white/90">{m.val}</p>
              </div>
            </div>
          ))}
        </div>
      </div>

      {/* Report sections */}
      {generating && (
        <div className="flex flex-col items-center py-10 gap-3">
          <RefreshCw className="w-6 h-6 animate-spin text-blue-400" />
          <p className="text-sm text-white/40">
            Generating report with Claude AI…
          </p>
          <p className="text-xs text-white/20">Aggregating event data, alerts, crew reports</p>
        </div>
      )}

      {sections.length > 0 && (
        <div className="space-y-2" id="after-action-report">
          {generatedAt && (
            <p className="text-[10px] text-white/25">
              Generated {generatedAt.toLocaleString()} · ExfSafeGrid AI Report Engine
            </p>
          )}
          {sections.map((section, idx) => (
            <div key={section.title} className="rounded-xl border border-white/[0.08] overflow-hidden">
              <button
                onClick={() => toggleSection(idx)}
                className="w-full flex items-center justify-between px-4 py-3 bg-white/[0.04] hover:bg-white/[0.06] transition-colors"
              >
                <span className="text-sm font-semibold text-white/80">{section.title}</span>
                {section.expanded
                  ? <ChevronUp className="w-4 h-4 text-white/30" />
                  : <ChevronDown className="w-4 h-4 text-white/30" />
                }
              </button>
              {section.expanded && (
                <div className="px-4 py-3 bg-white/[0.01]">
                  <pre className="text-sm text-white/60 whitespace-pre-wrap font-sans leading-relaxed">
                    {section.content}
                  </pre>
                </div>
              )}
            </div>
          ))}
        </div>
      )}

      {sections.length === 0 && !generating && (
        <div className="text-center py-12 border border-dashed border-white/10 rounded-xl">
          <TrendingUp className="w-8 h-8 text-white/15 mx-auto mb-3" />
          <p className="text-white/30 text-sm">Select an event and click "Generate Report"</p>
          <p className="text-white/15 text-xs mt-1">
            Powered by Claude AI · CPUC/NERC compliant format
          </p>
        </div>
      )}
    </div>
  );
}
